int main()
{
// Объявляем переменные
    float x = -3.0f,
          y;

/* Задаём функции для вычисления 
y при разных значениях x */
    if (x < -1)
    {
        y = -1 * x - 1;
    }
    else if (x >= -1 && x <= 0)
    {
        y = x + 1;
    }
    else if (x > 0 && x <= 1)
    {
        y = 1 - x;
    }
    else if (x > 1)
    {
        y = x - 1;
    }


return 0;
}
